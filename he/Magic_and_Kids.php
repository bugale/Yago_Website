<font size=6>
על קסמים, ילדים ומה שביניהם
<img src="PICS/yago.gif" width="80" height="144" alt="" />
</h3>
<font color=#00CCFF>
<font size=4>
<BR>
כ קוסם המופיע מזה שנים בפני ילדים ומבוגרים, אני שואל את עצמי - מה יש בהם בקסמים שהופך אותם למושכים כל כך בעיני ילדים?
</center>
<BR><BR>

<font color="yellow">
<font size=4>
ילדים אוהבים קסמים. הם פשוט מוקסמים מהם. אין זה מקרה שקסמים, קוסמים, מכשפות ופיות כיכבו מאז ומתמיד באגדות ובסיפורי הילדים. 
<BR>
אפילו כיום, בתקופה בה הטכנולוגיה נוגעת בכל פן של חיינו, חלקו של הקסם והפנטסיה בעולמם של ילדינו חשוב ובולט. ספר הילדים הנקרא ביותר הוא הארי פוטר והסרט שצולם על פיו מילא אולמות ברחבי העולם.
<BR><BR>
בגילאים צעירים, הילדים ממש נכנסים לתוך חווית הקסמים. בגילאים שלוש עד שש לערך, קיים אצלם עדיין ערבוב מסוים בין עולם הדמיון לבין עולם המציאות. הם מתלהבים  , מתרגשים ומאמינים שהם חווים קסם אמיתי,
<BR><BR>
בהופעותי בפני ילדים צעירים, אני מציג בפניהם בובה בצורת תוכי. הבובה זזה ומתעוררת לחיים בצורה כל כך משכנעת, עד כי אפילו הילדים הבוגרים יותר כבר לא בטוחים אם מדובר בבובה או בתוכי אמיתי.
<BR><BR>
בקטע אחר אני מצייר פרצוף על לוח ציור, הציור מתחיל לזוז ולדבר.  קשה לתאר בכתב את תגובתם של הילדים למראה הציור המדבר. הם פשוט "נטרפים" ומתגלגלים מצחוק. 
<BR><BR>
הילדים הבוגרים יותר נהנים בצורה אחרת. הם כבר פיתחו קורטוב של ציניות והקסמים מעוררים אצלם את יצר הסקרנות. במהלך ההופעה ולאחריה הם שואלים את עצמם - איך הוא עושה את זה? האם גם אני יכול לעשות זאת? כדי לענות על יצר הסקרנות שלהם, אני בדרך כלל מלמד אותם כמה טריקים פשוטים לאחר ההופעה. נפלא לראות אילו תלמידים נלהבים הם, וכמה הם צמאים ללמוד.
<BR><BR>
אין לי שום דבר נגד הפעלות אחרות לילדים אחרים, כמו משחקים היתוליים, ליצנים, מספרי סיפורים וכו'.  אבל מנסיון, ומנקודת המבט הלא אוביקטיבית שלי אני יכול לאמר - אין כמו קוסם להופעה של ילדים. הופעת קסמים מספקת לילדים חוויה אדירה, וזכרון שנשאר אצלם זמן רב.

